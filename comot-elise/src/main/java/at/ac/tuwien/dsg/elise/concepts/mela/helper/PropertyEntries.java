package at.ac.tuwien.dsg.elise.concepts.mela.helper;

import java.util.ArrayList;
import java.util.Collections;
import java.util.List;
import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlRootElement;

/**
 *
 * @Author Daniel Moldovan
 * @E-mail: d.moldovan@dsg.tuwien.ac.at
 *
 */
@XmlAccessorType(XmlAccessType.FIELD)
@XmlRootElement(name = "Property")
public class PropertyEntries {

    @XmlElement(name = "entry")
    private List<PropertyEntry> entries = new ArrayList<PropertyEntry>();

    List<PropertyEntry> entries() {
        return Collections.unmodifiableList(entries);
    }

    void addEntry(PropertyEntry entry) {
        entries.add(entry);
    }
}